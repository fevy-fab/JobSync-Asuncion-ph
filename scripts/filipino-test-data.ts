/**
 * Generate work experience based on education
 */
export function generateWorkExperience(educationEndYear: number) {
  const experiences = [];
  const numJobs = Math.floor(Math.random() * 3) + 1; // 1-3 jobs
  let currentYear = educationEndYear;

  for (let i = 0; i < numJobs; i++) {
    const company = COMMON_COMPANIES[Math.floor(Math.random() * COMMON_COMPANIES.length)];
    const position = JOB_TITLES[Math.floor(Math.random() * JOB_TITLES.length)];
    const yearsInJob = Math.floor(Math.random() * 3) + 1; // 1-3 years per job

    const from = `${currentYear}-${String(Math.floor(Math.random() * 12) + 1).padStart(2, '0')}-01`;
    currentYear += yearsInJob;
    const to = i === 0 ? 'Present' : `${currentYear}-${String(Math.floor(Math.random() * 12) + 1).padStart(2, '0')}-28`;

    const monthlySalary = (15000 + Math.floor(Math.random() * 35000)).toString();

    experiences.push({
      from,
      to,
      position,
      company,
      monthlySalary,
      salaryGrade: '',
      status: 'Full-time',
      governmentService: Math.random() > 0.7
    });
  }

  return experiences.reverse(); // Most recent first
}

/**
 * Generate random skills
 */
export function generateSkills(): string[] {
  const numSkills = Math.floor(Math.random() * 8) + 5; // 5-12 skills
  const allSkills = [...SKILLS_POOL, ...TECHNICAL_SKILLS];
  const skills: string[] = [];

  while (skills.length < numSkills) {
    const skill = allSkills[Math.floor(Math.random() * allSkills.length)];
    if (!skills.includes(skill)) {
      skills.push(skill);
    }
  }

  return skills;
}

