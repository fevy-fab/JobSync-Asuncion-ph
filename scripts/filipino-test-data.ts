/**
 * Generate a random Filipino full name
 */
export function generateFilipnoName(gender: 'male' | 'female' = 'male'): {
  firstName: string;
  middleName: string;
  lastName: string;
  fullName: string;
  suffix?: string;
} {
  const firstNames = gender === 'male' ? FILIPINO_MALE_NAMES : FILIPINO_FEMALE_NAMES;
  const firstName = firstNames[Math.floor(Math.random() * firstNames.length)];
  const middleName = FILIPINO_SURNAMES[Math.floor(Math.random() * FILIPINO_SURNAMES.length)];
  const lastName = FILIPINO_SURNAMES[Math.floor(Math.random() * FILIPINO_SURNAMES.length)];

  // Avoid same middle and last name
  let finalMiddleName = middleName;
  if (middleName === lastName) {
    finalMiddleName = FILIPINO_SURNAMES[Math.floor(Math.random() * FILIPINO_SURNAMES.length)];
  }

  const suffixes = ['', '', '', '', 'Jr.', 'III'];
  const suffix = suffixes[Math.floor(Math.random() * suffixes.length)];

  const fullName = suffix
    ? `${firstName} ${finalMiddleName} ${lastName} ${suffix}`
    : `${firstName} ${finalMiddleName} ${lastName}`;

  return {
    firstName,
    middleName: finalMiddleName,
    lastName,
    fullName,
    suffix: suffix || undefined
  };
}

/**
 * Generate a random Philippine address
 */
export function generatePhilippineAddress(): {
  street: string;
  barangay: string;
  city: string;
  province: string;
  zipCode: string;
  fullAddress: string;
} {
  const houseNumber = Math.floor(Math.random() * 999) + 1;
  const streetName = STREET_NAMES[Math.floor(Math.random() * STREET_NAMES.length)];
  const barangay = BARANGAYS[Math.floor(Math.random() * BARANGAYS.length)];

  const isMetroManila = Math.random() > 0.3;
  const city = isMetroManila
    ? METRO_MANILA_CITIES[Math.floor(Math.random() * METRO_MANILA_CITIES.length)]
    : 'Asuncion';

  const province = isMetroManila
    ? 'Metro Manila'
    : NEARBY_PROVINCES[Math.floor(Math.random() * NEARBY_PROVINCES.length)];

  const zipCode = isMetroManila
    ? String(1000 + Math.floor(Math.random() * 800))
    : String(4000 + Math.floor(Math.random() * 900));

  const street = `${houseNumber} ${streetName}`;
  const fullAddress = `${street}, ${barangay}, ${city}, ${province} ${zipCode}`;

  return { street, barangay, city, province, zipCode, fullAddress };
}

